//!
//! The file extensions.
//!

/// The manifest file extension.
pub static MANIFEST: &str = "toml";

/// The JSON data file extension.
pub static JSON: &str = "json";

/// The Zinc source code file extension.
pub static ZINC: &str = "zn";

/// The Yul source code file extension.
pub static YUL: &str = "yul";

/// The EVM file extension.
pub static EVM: &str = "evm";

/// The EVM bytecode file extension.
pub static EVM_BINARY: &str = "bin";

/// The Solidity source code file extension.
pub static SOLIDITY: &str = "sol";

/// The Vyper source code file extension.
pub static VYPER: &str = "vy";

/// The LLVM source code file extension.
pub static LLVM_SOURCE: &str = "ll";

/// The LLVM bitcode file extension.
pub static LLVM_BINARY: &str = "bc";

/// The zkEVM assembly file extension.
pub static ZKEVM_ASSEMBLY: &str = "zasm";

/// The zkEVM binary file extension.
pub static ZKEVM_BINARY: &str = "zbin";

/// The ABI file extension.
pub static ABI: &str = "abi";
